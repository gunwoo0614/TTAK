// X^2 - 2 = 0의 근 찾기 테스트용 함수들
typedef double (*func_t)(double);

// 테스트용 함수: f(x) = x^2 - 2
static double test_func_sqrt2(double x) {
    return x * x - 2.0;
}

// 일반적인 이진탐색 함수 (테스트용)
// f(x) = 0을 만드는 x를 찾음
static bool bisection_general(func_t f, double lo, double hi, double eps, int max_iter, double* root_out)
{
    if (!root_out || !isfinite(lo) || !isfinite(hi) || !isfinite(eps) || eps <= 0.0)
        return false;
    if (max_iter <= 0) return false;
    if (!f) return false;

    if (lo > hi) { double t = lo; lo = hi; hi = t; }

    double flo = f(lo);
    double fhi = f(hi);
    if (!isfinite(flo) || !isfinite(fhi)) return false;

    // 이미 근이면 바로 반환
    if (fabs(flo) <= eps) { *root_out = lo; return true; }
    if (fabs(fhi) <= eps) { *root_out = hi; return true; }

    // 이분법을 사용하려면 부호가 달라야 함
    if (flo * fhi > 0.0) return false;

    for (int i = 0; i < max_iter; i++) {
        double mid = 0.5 * (lo + hi);
        double fmid = f(mid);
        if (!isfinite(fmid)) return false;

        if (fabs(fmid) <= eps || 0.5 * (hi - lo) <= eps) {
            *root_out = mid;
            return true;
        }

        if (flo * fmid < 0.0) {
            hi = mid;
            fhi = fmid;
        }
        else {
            lo = mid;
            flo = fmid;
        }
    }

    // max_iter까지 반복했지만 수렴
    *root_out = 0.5 * (lo + hi);
    return true;
}
